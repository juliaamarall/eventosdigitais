                                    <p>Dentre alguns benefícios do evento digital, seja híbrido ou não, podemos destacar <strong>o custo-benefício, acessibilidade, exponenciação da cobertura geográfica do evento, sustentabilidade e facilidade de acesso ao conteúdo pós-evento</strong>.</p>

                                    <p>Disponibilizar o conteúdo pós-evento é uma prática comum nos eventos digitais que tende a fortalecer a marca do evento, mas tende a <strong>reduzir o engajamento ao vivo</strong>, pois a tendência de evasão em tempo real aumenta.</p>

                                    <p>Aproveitamos esse exemplo para apresentar essa nova peça no tabuleiro dos eventos digitais. Agora você decide se o evento será síncrono (ao vivo), gravado ou uma mistura dos dois.</p>

                                    <p>Quando o evento não disponibiliza a gravação, a tendência de engajamento e presença ao vivo aumenta, porém pode gerar frustração em quem deduz que terá acesso ao conteúdo posteriormente.</p>

                                    <p>Você pode optar também por deixar as palestras gravadas durante um período pré-determinado após o evento ou <strong>monetizar o conteúdo não assistido ao vivo </strong>após a sua realização. Esta última opção não é muito observada, exceto em campanhas de venda de cursos EaD</p>

                                    <p>Outra possibilidade consiste no <strong>planejamento de uma experiência de conteúdo híbrida</strong>, na qual o palestrante grava um vídeo que será enviado aos participantes (assíncrono) e posteriormente realiza uma sessão ao vivo de perguntas e respostas (síncrona).</p>

