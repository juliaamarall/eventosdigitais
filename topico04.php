                                                </em></td>
                                            </tr>

                                            <tr class="tBody">
                                                <td data-th=""><em>Encontro</em></td>
                                                <td data-th="Plataforma de Captação"><em>Evento onde pessoas da mesma categoria profissional e interesses comuns debates temas polêmicos. Pode ser estadual, regional, nacional ou internacional.</em></td>
                                            </tr>

                                            <tr class="tBody">
                                                <td data-th=""><em>Jornada</em></td>
                                                <td data-th="Plataforma de Captação"><em>
