<p class = 'text-left'>
Mas afinal, qual a tipologia do Rock In Rio? Festival, Curso ou Educação? Tudo isso junto? O festival atualmente se posiciona como uma plataforma, uma experiência que permeia vários formatos e canais.
</p>

<p class = 'text-left'>
A inspiração nesse projeto é válida, mas é preciso delimitar o alcance do seu evento e ter um foco claro. O seu evento será um curso? Um fórum? Uma mistura de conferência e convenção?
</p>

<p class="text-left">Assim como Rock in Rio, seu evento deve estar mais preocupado com a experiência do que as tipologias, mas é fundamental conhecê-las e se apropriar das possibilidades já existentes e herdadas da modalidade presencial.</p>

<p class="text-left">Cada tipo de evento tem suas regras próprias que podem ser pesquisadas em sites ou livros específicos. O propósito desse curso é imersar no universo de eventos digitais, da criação à execução. Por isso, abordaremos às tipologias de forma resumida.</p>

<p class="text-left">O tempo e a dinâmica deve ser adaptados para o ambiente digital. Um seminário presencial de uma hora, por exemplo, pode ser adaptado para quarenta minutos ou menos, seguido por perguntas e respostas.</p>

<p class="text-left">É bastante comum o uso inadequado de tipologias nos eventos presenciais e digitais, então decidimos listar algumas tipologias que podem ser utilizadas em eventos educacionais.</p>

<p class="text-left">Note que algumas tipologias são sociais ou culturais. São categorias não relacionadas diretamente com a educação, mas que podem fazer parte do evento em momentos específicos através de intervenções artísticas.</p>

<p class="text-left">Resumimos e esclarecemos algumas tipologias com base em (Zitta, 2014):</p>
